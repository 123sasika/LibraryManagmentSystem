    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String selectedBookId = (String) jComboBox1.getSelectedItem();
        if (selectedBookId.isEmpty()) {
            JOptionPane.showMessageDialog(null, "ID cannot be empty!");
            return;
        }

        try {
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
            
            String query = "DELETE FROM book WHERE ID = ?";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1,selectedBookId);
            int rowsDeleted = pst.executeUpdate();  //execute the above code

            if (rowsDeleted== 0) {
                throw new BookNotFoundException("Book "+selectedBookId+" was not found");
            } else {
                JOptionPane.showMessageDialog(null, "Book was deleted successfully!");
            }
            
            conn.close();
        }catch(BookNotFoundException e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
        new DeleteBook().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

