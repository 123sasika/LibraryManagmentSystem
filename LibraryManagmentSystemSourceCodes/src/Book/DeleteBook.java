    public void table(){
        try{
        Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
        PreparedStatement pst=conn.prepareStatement("select*from book");
        ResultSet bookRs = pst.executeQuery();
                 
        model.setRowCount(0);    
        while (bookRs.next()) {
                String book_id = bookRs.getString("ID");
                String title = bookRs.getString("Title");
                String author = bookRs.getString("Author");
                String genre = bookRs.getString("Genre");
                String isbn = bookRs.getString("ISBN");
                model.addRow(new Object[]{book_id,title,author,isbn,genre});   
        }
        conn.close();
        }   
        catch(Exception e){
            e.printStackTrace();
        }
    }
    
    public void fetch(){
          try {
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT ID FROM book");
           
            while (rs.next()) {
                jComboBox1.addItem(rs.getString("ID"));
            }
           table();                     
           conn.close();
        } 
        catch (SQLException e) {
            e.printStackTrace();
        }
     }

