    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
        //fetching data from the user and adding it to the database, and connection database
        String ID=jTextField1.getText();
        String Title=jTextField2.getText();
        String Author=jTextField3.getText();
        String Genre=jTextField4.getText();
        String ISBN=jTextField5.getText();       
        Book obj = new Book(ID,Title,Author,Genre,ISBN);  //creating Book object  
        try{
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
            String query="insert into book values('"+obj.ID2()+"','"+obj.Title2()+"','"+obj.Author2()+"','"+obj.Genre2()+"','"+obj.ISBN1()+"')";
            PreparedStatement pst=con.prepareStatement(query);
            int Result=pst.executeUpdate();

        if(Result==0){
            throw new BookNotAddException("The book was not added");
        }else{
            JOptionPane.showMessageDialog(jButton1,"successfully updated"); // informing to user
        }
            loadData(); //updating the table in real time
            con.close();// closing connection
        }
        
        catch(BookNotAddException e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        catch(Exception e)
                {
                JOptionPane.showMessageDialog(jButton1,"Book ID already exist"); // informing to user
                }
        
         new AddBook().setVisible(true); //nevigating user to AddBook page when user click the button
        this.dispose();   //closing this JFrame when user click the button
    }//GEN-LAST:event_jButton1ActionPerformed

