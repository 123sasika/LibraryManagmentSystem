    //creating a method to add data from the database to the Java GUI table and connectiong database to the Java GUI
    public void loadData() {
       
    try{
        Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");       
        tableModel.setRowCount(0); // Clear the existing rows in the table       
        String bookQuery = "select ID,Title,Author,Genre,ISBN from book"; // Query for books table 
        PreparedStatement bookStmt = conn.prepareStatement(bookQuery); //preparing the SQL query to execute
        ResultSet bookRs = bookStmt.executeQuery();  //executing the query and returning result

        // Fetching book data and add to variables
        while (bookRs.next()) {
                String book_id = bookRs.getString("ID");
                String title = bookRs.getString("Title");
                String author = bookRs.getString("Author");
                String genre = bookRs.getString("Genre");
                String isbn = bookRs.getString("ISBN");
        // Adding book rows to the table
        tableModel.addRow(new Object[]{book_id, title, author, genre, isbn});
        }
        // Closing connections              
        conn.close();
        }
    catch (SQLException e) {
            e.printStackTrace();
        }
    }   
